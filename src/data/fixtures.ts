const prediction = (
  symbol: string,
  label: Prediction["label"],
  confidence: number,
  risk: number,
  gain: number,
  loss: number,
  thesis: string[]
): Prediction => ({
  id: `pred-${symbol.toLowerCase()}`,
  symbol,
  createdAt: ingestionTime,
  horizon: "5D",
  label,
  confidence,
  risk,
  safety: Math.max(1, 100 - risk),
  possibleGainPercent: gain,
  possibleLossPercent: loss,
  uncertainty:
    "This research estimate can be wrong. It depends on stored demo market data, news tone, volatility, and signal rules.",
  thesis,
  invalidation: "Signal weakens if price closes below support or news tone materially deteriorates.",
  status: "Open",
  outcome: "Pending"
});

