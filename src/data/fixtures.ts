const buildBars = (base: number, drift: number, volatility: number): Bar[] => {
  const bars: Bar[] = [];
  const start = Date.UTC(2021, 5, 15);
  for (let index = 0; index < 260; index += 1) {
    const wave = Math.sin(index / 8) * volatility + Math.cos(index / 19) * volatility * 0.7;
    const close = Number((base + index * drift + wave).toFixed(2));
    const open = Number((close - Math.sin(index / 5) * volatility * 0.35).toFixed(2));
    const high = Number((Math.max(open, close) + volatility * 0.55 + (index % 7) * 0.03).toFixed(2));
    const low = Number((Math.min(open, close) - volatility * 0.5 - (index % 5) * 0.02).toFixed(2));
    bars.push({
      time: new Date(start + index * 24 * 60 * 60 * 1000).toISOString().slice(0, 10),
      open,
      high,
      low,
      close,
      volume: Math.round(18_000_000 + index * 41_000 + Math.abs(Math.sin(index / 4)) * 7_000_000)
    });
  }
  return bars;
};

